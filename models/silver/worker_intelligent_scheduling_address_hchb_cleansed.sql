     WHEN NULLIF(TRIM(TRIM("WISA_ZIP")), '') IS NULL THEN NULL
     ELSE REGEXP_REPLACE(NULLIF(TRIM(TRIM("WISA_ZIP")), ''), '[^0-9]+', '')
 END AS "WISA_ZIP",
