    TRY_TO_TIMESTAMP(NULLIF(TRIM(TRIM("CDC_TS")), '')) AS "CDC_TS",
    INITCAP(REGEXP_REPLACE(NULLIF(TRIM(TRIM("WISA_CITY")), ''), '[^A-Za-z ]+', '')) AS "WISA_CITY",
