    NULLIF(TRIM(TRIM("WISA_COUNTY")), '') AS "WISA_COUNTY",
    NULLIF(TRIM(TRIM("WISA_LATITUDE")), '') AS "WISA_LATITUDE",
    NULLIF(TRIM(TRIM("WISA_ID")), '') AS "WISA_ID",
    NULLIF(TRIM("INSERTED_DATE"), '') AS "INSERTED_DATE",
    NULLIF(TRIM(TRIM("INSERTED_BY")), '') AS "INSERTED_BY",
